#Faça um Programa que peça as 4 notas bimestrais e mostre a média.
nota1 = input("Digite a nota do bimestre 1: ")
nota2 = input("Digite a nota do bimestre 2: ")
nota3 = input("Digite a nota do bimestre 3: ")
nota4 = input("Digite a nota do bimestre 4: ")

media = (int(nota1) + int(nota2) + int(nota3) + int(nota4))/4

print(f"A média é {media}")
